package fundamentos;

public class Temperatura {
	public static void main(String[] args) {
		final double X = 32;
		final double Y = 5.0/9.0;
		double f = 86;
		double c = (f - X) * Y;
		System.out.println("O resultado é " + c + "°C.");

		f = 180;
		c = (f - X) * Y;
		System.out.println("O resultado é " + c + "°C.");
		
		
	   
		
		
	}
}
